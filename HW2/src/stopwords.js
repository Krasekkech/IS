function normalizeStopword(w) {
    return w
        .toLowerCase()
        .replace(/ё/g, "е")
        .replace(/-/g, "_");
}

export function getStopwordsSet() {
    return new Set(RAW_STOPWORDS_RU.map(normalizeStopword));
}